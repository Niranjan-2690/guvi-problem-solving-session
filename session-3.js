// 1 (a). Reverse a string.

function reverseString(ipt){
    let reverse = []
    for(let i = 0; i<ipt.length; i++){
        reverse += ipt[ipt.length -i -1]
    }
    return reverse
}

console.log(reverseString("NIRANJAN"))

